char	*ft_strrchr(const char *s, int c)
{
	char	*r;

	r = 0;

	while (*s != '\0')
	{
		if (*s == c)
			*r = ((char *)s);
		else
			*s++;
	}
	if (*s == '\0')
		return (r);
	return (0);
}
